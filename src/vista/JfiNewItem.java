    private void BtnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnRegistrarActionPerformed
        Item item = new Item();
        ConItem metodItem = new ConItem();
        String[] split = ComboCatItem.getSelectedItem().toString().split("-");
        String IdCategoria = split[0];

        if (InputCodItem.getText().trim().isEmpty() || InputNomItem.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Es necesario ingresar un codigo y nombre de item");
        } else if (!metodItem.conItem(InputNomItem.getText(), IdCategoria).getNom_item().isEmpty()) {
            JOptionPane.showMessageDialog(null, "El item ya se encuentra registrado en dicha categoria");
        } else {
            item.setId_item(InputCodItem.getText());
            item.setNom_item(InputNomItem.getText());
            item.setDes_item(InputDesItem.getText());
            item.setId_categoria(IdCategoria);
            //true
            if (metodItem.InsItem(item)) {
                JOptionPane.showMessageDialog(null, "Se registro correctamente");
                InputCodItem.setText("");
                InputDesItem.setText("");
                InputNomItem.setText("");
            } else {
                JOptionPane.showMessageDialog(null, "Error al registrar");
            }
        }

    }//GEN-LAST:event_BtnRegistrarActionPerformed


